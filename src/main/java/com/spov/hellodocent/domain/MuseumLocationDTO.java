package com.spov.hellodocent.domain;

public class MuseumLocationDTO {

	public MuseumLocationDTO() {
		super();
	}

	String epl_id;
	String epl_address;
	String epl_addressRoad;
	String epl_token;
	String epl_locationX;
	String epl_locationY;

	public String getEpl_id() {
		return epl_id;
	}

	public void setEpl_id(String epl_id) {
		this.epl_id = epl_id;
	}

	public String getEpl_address() {
		return epl_address;
	}

	public void setEpl_address(String epl_address) {
		this.epl_address = epl_address;
	}

	public String getEpl_token() {
		return epl_token;
	}

	public void setEpl_token(String epl_token) {
		this.epl_token = epl_token;
	}



	
	public String getEpl_locationX() {
		return epl_locationX;
	}

	public void setEpl_locationX(String epl_locationX) {
		this.epl_locationX = epl_locationX;
	}

	public String getEpl_locationY() {
		return epl_locationY;
	}

	public void setEpl_locationY(String epl_locationY) {
		this.epl_locationY = epl_locationY;
	}

	public String getEpl_addressRoad() {
		return epl_addressRoad;
	}

	public void setEpl_addressRoad(String epl_addressRoad) {
		this.epl_addressRoad = epl_addressRoad;
	}
	
	

}
